package Controle;

public class For1 {
    public static void main(String[] args) {
        int contador =1;

        while(contador <= 10){
            System.out.println("Bom dia!");
            contador ++;
        }
        for(int i =1; i <= 10; i++){
            System.out.println("Bom dia!");
        }
    }
}
